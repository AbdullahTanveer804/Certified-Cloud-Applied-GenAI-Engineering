"""
Program: add2numbers
--------------------
A python program to get some practice with
variables.  This program asks the user for two
integers and prints their sum.
"""


def main()-> int :
  num1 = int(input("Enter first number: "))
  num2 = int(input("Enter second number: "))
  sum = num1 + num2
  print(sum)
main()
    