console.log('Hello, world!');
// Operators
// Arithmetic operators
// +, -, *, /, %, ++, --
let a = 10;
let b = 20;
console.log(a + b); // 30
console.log(a - b); // -10
console.log(a * b); // 200
console.log(a / b); // 0.5
console.log(a % b); // 10
console.log(a++); // 10
console.log(a); // 11
console.log(b--); // 20
console.log(b); // 19
const c = 'Hello, ';
const d = 'world!';
console.log(c + d); // Hello, world!
const e = 10;
const f = '20';
console.log(e + f); // 1020
const g = 10;
const h = '20';
console.log(g + Number(h)); // 30
const i = 10;
const j = '20';
console.log(i + parseInt(j)); // 30
const k = 10;
const l = '20.11';
console.log(k + parseFloat(l)); // 30.11
const m = 10;
const n = '20.11';
console.log(m + +n); // 30.11
